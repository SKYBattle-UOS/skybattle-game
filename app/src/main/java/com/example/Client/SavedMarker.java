package com.example.Client;

class SavedMarker {
    public double lat;
    public double lon;
    public int color;
    public float size;
    public String name;

    public SavedMarker(double lat, double lon, int color, float size, String name){
        this.lat = lat;
        this.lon = lon;
        this.color = color;
        this.size = size;
        this.name = name;
    }
}
